// Helper to get text based on language
export const getText = (item, language = 'en') => {
    if (typeof item === 'string') return item;
    return item[language] || item.en;
};

// Get random truth based on intensity
export const getRandomTruth = (intensity = 'mild', language = 'en') => {
    const truths = TRUTHS[intensity] || TRUTHS.mild;
    const truth = truths[Math.floor(Math.random() * truths.length)];
    return getText(truth, language);
};

// Get random dare based on intensity
export const getRandomDare = (intensity = 'mild', language = 'en') => {
    const dares = DARES[intensity] || DARES.mild;
    const dare = dares[Math.floor(Math.random() * dares.length)];
    return getText(dare, language);
};

// Get all intensity levels
