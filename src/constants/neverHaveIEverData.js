// Helper to get text based on language
export const getText = (item, language = 'en') => {
    if (typeof item === 'string') return item;
    return item[language] || item.en;
};

// Get random statement based on intensity
export const getRandomStatement = (intensity = 'mild', language = 'en') => {
    const statements = STATEMENTS[intensity] || STATEMENTS.mild;
    const statement = statements[Math.floor(Math.random() * statements.length)];
    return getText(statement, language);
};

// Get all statements for a given intensity
export const getAllStatements = (intensity = 'mild', language = 'en') => {
    const statements = STATEMENTS[intensity] || STATEMENTS.mild;
    return statements.map(s => getText(s, language));
};

// Get intensity levels
export const getIntensityLevels = (language = 'en') => {
    return INTENSITY_LEVELS.map(level => ({
        ...level,
        name: getText(level.name, language),
        description: getText(level.description, language),
    }));
};
